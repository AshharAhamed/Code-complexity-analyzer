
 class InheritanceSample {
	 class Vehicle {
		public void display() {
			System.out.println("This is Vehicle class");
		}
	}

	 class Car extends Vehicle {
		public void display() {
			System.out.println("This is Vehicle class");
		}
	}

	 class SportsCar extends Car {
		public void display() {
			System.out.println("This is Vehicle class");
		}
	}
}
